edad = 26 # Entero - Sin punto decimal
estatura = 1.75 # Float - Con punto decimal
print(edad + estatura) # Suma
print(edad - estatura) # Resta
print(edad * estatura) # Multiplicación
print(edad / estatura) # División
print(edad % estatura) # Modulo 
print(edad // estatura) # División enterea
print(edad ** 2) # Potencia